            <div class="content_txt">
            	<h2>Victor Vekselberg, Skolkovo</h2>
                <p>
                Victor Vekselberg is the president of the Skolkovo Foundation, appointed to the position in March 2010 by President Dmitry Medvedev of the Russian Federation to head the Russian section of the Skolkovo Innovation Centre Project.<br/><br/>
                Previously, Vekselberg was chairman of the Board of Directors of United Company Rusal (UC Rusal), an aluminum industry leader, from 2007 - 2010. Prior, Vekselberg was the Chairman of Tyumen Oil (TN) from 2002 - 2003 and is currently a member of the Board of Directors of TNK-BP.<br/><br/> 
                Vekselberg co-founded SUAL Holding in 1996, serving as president of the SUAL Group until 2003 and becoming the chairman of the Board of Directors. Vekselberg also founded investment and business development company Renova in 1990, which was initially a Russian-American joint enterprise, and has continued to serve as chairman of the Board of Directors.<br/><br/>
                In addition to his current roles, Vekselberg is a member of the Russian Union of Industrialists and Entrepreneurs (RSPP) and chairman of the Committee on International Cooperation of RSPP.<br/><br/>
                Vekselberg is a native of the Ukraine and graduated magna cum laude from the Moscow Transportation Engineering Institute. He completed post-graduate studies in the Dorodnicyn Computing Centre of Russian Academy of Sciences and holds a Ph.D in mathematical sciences.                
                </p>
