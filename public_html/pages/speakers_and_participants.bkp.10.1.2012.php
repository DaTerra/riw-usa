            	<h2>Anatoly Chubais, RUSNANO</h2>
                <p>Anatoly Chubais serves as the chief executive officer of Russian Corporation of Nanotechnologies, commonly known as RUSNANO. Before his appointment in 2008, Chubais served as the CEO of state-owned energy monopoly RAO Unified Energy Systems of Russia for a decade.<br/><br/> 
                Previously, Chubais held a wide range of top-level positions in the Russian government. From 1995 to 1996, Chubais represented Russia in international financial organizations, and in 1996, Chubais was appointed chief of staff of the Presidential Executive Office. Additional high-level positions included simultaneous appointments as first deputy prime minister of the Russian Federation, minister of finance and Russia's first deputy prime minister of economic and financial policy.<br/><br/>
                Early in his career, Chubais was an engineer and an assistant and associate professor at the Leningrad Institute of Engineering and Economics.<br/><br/> 
                Chubais has received several honors, including being named the Best Financial Minister of the Year in 1997 from Euromoney magazine and receiving a prestigious award from the International Union of Economists.<br/><br/>
                Chubais received a Ph.D. from the Palmiro Togliatti Leningrad Institute of Engineering and Economics. He is also a graduate of the Moscow Energy Engineering Institute, where he specialized in problems of contemporary energy generation. Chubais also received an honorary Ph.D. from Saint Petersburg State University of Engineering and Economics.  
                </p>
