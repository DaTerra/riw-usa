            	<h2>Alexandra Johnson, DFJ VTB Aurora</h2>
                <p>
                Alexandra Johnson is a Managing director of DFJ VTB Aurora, a venture group specializing in investments in Russia and the Commonwealth of Independent States. It represents a strategic partnership between two of the world’s most respected financial institutions: Silicon Valley’s Draper Fisher Jurvetson (<a href="www.dfj.com" target="_blank">www.dfj.com</a>) and Russia’s VTB Capital (<a href="http://www.vtb.com/" target="_blank">www.vtb.com</a>).<br/><br/>
