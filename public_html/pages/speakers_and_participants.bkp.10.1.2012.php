                Alexandra runs the Global Technology Symposium (GTS), held annually in Silicon Valley since 2003 and is a private sector Co-Chair of the U.S. – Russia Innovation Working Group. <br/><br/>
                Alexandra received an M.A. with honors from the Far Eastern State University, Vladivostok and a Ph.D. from University of St. Petersburg. She also has an M.B.A. from the University of California, Berkeley. <br/><br/>           
