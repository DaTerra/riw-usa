            	<h2>Lorraine Hariton, U.S. Special Representative for Commercial and Business Affairs</h2>
                <p>
             Lorraine Hariton was sworn in as the State Department's Special Representative for Commercial and Business Affairs on Sept. 14, 2009. Her experience in innovation and entrepreneurship will provide a unique perspective in addressing our global economic growth challenges. She will be responsible for State Department outreach to the business community and commercial advocacy efforts. She will work with the business community worldwide to ensure that commercial diplomacy efforts support U.S. foreign policy objectives.
<br/><br/>
Hariton brings to the State Department more than 25 years of experience in the information technology sector in Silicon Valley. Throughout her business career Hariton worked with various technologies and products including mainframes, thin-client technology, e-commerce financial infrastructure, Internet audio solutions and speech applications. Most recently Hariton worked on Smart Grid initiatives including advanced metering and intelligent street lighting.
<br/><br/>
Hariton served as President and CEO of Apptera from 2003 to 2005 where she brought their first product to market and raised $8 million in venture capital. She served as the CEO of Beatnik from 1999 to 2002 where she repositioned the company as a leader in the wireless industry and raised more than $40 million in venture capital. Hariton also spent 15 years at IBM serving in a number of executive capacities.
<br/><br/>
Hariton is Chairman Emeritus of the Forum for Women Entrepreneurs and Executives, served as Treasurer of the State of California Board of Accountancy, served on the board of the Demand Response Smart Gird Coalition, served on the National Advisory Board of the Stanford Clayman Institute for Gender Research and the Executive Committee of the National Center for Women and Information Technology.
<br/><br/>
Hariton has an M.B.A. from Harvard Business School and a B.S. in Mathematical Sciences from Stanford University.

<br/><br/>           
              </p>
