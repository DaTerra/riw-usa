            	<h6>About the Skolkovo Foundation</h6>
                <p>
                The Skolkovo Foundation is a non-profit organization whose mission is to accelerate the transformation of Russia into an innovation-based economy through strategic partnerships with leading scientists and innovators around the world. Founded in May 2010 by the Russian government, the Skolkovo Foundation strives to turn cutting-edge research into world-class products by providing the ideal environment, R&D infrastructure, and scientific and education resources to facilitate innovation in five key business clusters: IT, Energy Efficiency, Nuclear Technology, Biomedicine and Space.<br/><br/>
                
                The Skolkovo Innovation Centre, a physical city currently under construction just outside of Moscow, is a physical manifestation of Skolkovo's mission - a global technology hub that represents Russia's commitment to creating the knowledge-based enterprise economy of tomorrow. The city, which will play host to the 2014 G8 Summit, includes a Technopark research facility, offices for participating companies and startups, and education facilities including the Skolkovo Institute of Science and Technology (SkTech), a joint venture between the Foundation and the Massachusetts Institute of Technology (MIT).<br/><br/>
                
                Skolkovo is a proud member of the Russian Innovation Center and a key sponsor for Russian Innovation Week 2012.<br/><br/>
                
