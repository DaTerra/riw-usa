           		<h6>About RVC </h6>
                <p>
                   RVC is a government fund of funds that channels public incentives to venture capitalists and support to the hi-tech sector. RVC was founded by the Russian government in 2006 to ensure faster development of an efficient and globally competitive innovation system and venture capital industry in Russia.<br/><br/>
                   To fulfill its mission, RVC engages private venture capitalists; nurtures innovative entrepreneurship, technology and business expertise; and mobilizes Russian human resources. RVC currently backs 12 venture capital funds that run a portfolio of 118 companies. RVC-backed venture capital funds invest predominantly in technologies such as security and counterterrorism, biotech, energy, life sciences, IT and aerospace. At least 80 percent of the RVC's resources are invested in early-stage startups.<br/><br/>
