            	<h2>Kirill Tatarinov , Microsoft Business Solutions Division</h2>
                <p>
                Kirill Tatarinov is president of the Microsoft Business Solutions Division (MBS) with responsibility for MBS research, development, sales, marketing, and operations. MBS develops and markets a portfolio of Microsoft Dynamics products and services covering a broad range of functions including financial, customer relationship and supply chain management (ERP and CRM) that bring simplicity, value and agility to organizations of all sizes. MBS also includes Microsoft's Health Solutions Group, which develops, sells and supports software solutions for improving the quality and efficiency of healthcare delivery and empowering individuals to become more engaged in their own health and wellness.<br/><br/>  
                Prior to joining MBS in 2007, Tatarinov led the Management and Solutions Division at Microsoft, where he was in charge of the Microsoft Windows management technologies and products, including Microsoft System Center, Systems Management Server, Microsoft Operations Manager and Microsoft Application Center, as well as Windows Server solutions, including Microsoft Small Business Server.<br/><br/>  
                Tatarinov joined Microsoft in 2002 with 15 years of experience in the software industry. Before joining Microsoft, Tatarinov was senior vice president and chief technology officer for BMC Software Inc. While at BMC he also had responsibility for corporate development and for Patrol Software business. Before that, Tatarinov was co-founder, chief architect, and head of R & D for Patrol Software, the developer of innovative software solutions for systems and network management acquired by BMC in 1994. Before co-founding Patrol Software, Tatarinov worked in several systems, networking and consulting companies in Russia, Israel and Australia.<br/><br/>  
                In January 2002, Computerworld named Tatarinov one of the business world's 2002 Premier 100 IT Leaders. This award honors individuals who have had a positive impact on their organizations through the use of technology.<br/><br/>  
                Tatarinov grew up in Moscow, Russia, and holds a master's diploma in systems engineering from Moscow University of Transport Engineering (MIIT) and an MBA from Houston Baptist University. He serves on the Seattle advisory council of the U.S. Fund for UNICEF, whose mission is to help the world's children.<br/><br/>  
                Tatarinov lives in the Seattle area with his family. Outside work, he spends his time skiing and is a Level-1 ski instructor, certified by the Professional Ski Instructors of America.                         
