            	<h2>Igor Agamirzian, RVC</h2>
                <p>
                Igor Agamirzian is the CEO of the Executive Committee of RVC. Before he joined RVC in May 2009, Agamirzian was general manager of EMC Corporation's software development center in St. Petersburg for two years. Prior to that, Agamirzian held several roles at Microsoft Russia from 1995 - 2007, including national technology officer, a member of the Board of Advisors to the chairman of Microsoft Russia, director of Government Relations and manager of Microsoft's relations with various computer science research institutions in Russia and Eastern Europe.<br /><br/>                
                Before joining Microsoft, Agamirzian was co-founder and technical director of Astro Soft, a St. Petersburg-based software developer and systems integrator. He also spent 12 years as a senior research fellow in the fields of programming languages and computer architecture with the USSR Academy of Sciences.<br /><br/>                
                Agamirzian has represented the Russian private sector on the G8's DOT Force, an international taskforce on the digital divide and in 2002 was appointed to the advisory board of the United Nations' ICT Task Force.                
