            	<h2>Conor Lenihan ,  Skolkovo Foundation</h2>
                <p>
                Conor Lenihan has served in several government ministries including Foreign Affairs, Justice& Interior, Integration, Education, Enterprise, Trade & Innovation. At the Department of Foreign Affairs he ran Irish Aid, the development cooperation wing of the department, which had a budget of up to one billion euro.<br/><br/> 
                Up to 2011 he was Ireland’s Minister for Science, Technology & Innovation where he created a single budget line for science and technology funding as well as participating in the country’s Innovation Task Force.<br/><br/>  
                He was first elected to the Irish parliament in 1997. Prior to his election he was a senior executive with an Irish-owned mobile operator and subsequently worked as an advisor to O2. In the 1990s he launched two FM radio stations, one in Dublin and the other in the Czech capital Prague. He began his working life as a newspaper journalist and continued into broadcasting.<br/><br/>  
                In the 1980s he was based in the Palace of Westminster where he was a Political Correspondent for the Irish News.<br/><br/>  
                He was educated at Belvedere College, University College Dublin, Dublin City University and INSEAD. He has a BA (Hons) degree in Economics, History and Politics and holds a post-graduate qualification in Communications.<br/><br/>  
                He is a member of one of the best known political families in Ireland with his grandfather, father, aunt and brother all having records of service in the Irish parliament and in key ministerial positions. His father, the late Brian Lenihan, held a number of cabinet portfolios and was Ireland’s deputy Prime Minister.         
              </p>
