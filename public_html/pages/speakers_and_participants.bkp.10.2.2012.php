            	<h2>Dr. Evgeny Zaytsev - Co-Founder and General Partner, Helix Ventures</h2>
                <p>
                Dr. Evgeny Zaytsev is a successful venture capitalist with twelve years’ investment experience in life sciences. Prior to co-founding Helix Ventures he was a partner at Asset Management Company, one of the oldest venture firms in Silicon Valley. Dr. Zaytsev was involved in many success stories of venture capital including BiPar Sciences (acquired by sanofi-aventis), Enteric Medical Technologies (acquired by Boston Scientific), Fusion Medical Technologies (acquired by Baxter International), MicroVention (acquired by Terumo Corporation), TriVascular (acquired by Boston Scientific). He is a Board Member of a number of companies in Silicon Valley, and an Advisory Council Member of the Russian Venture Company. <br/><br/>  
                Evgeny Zaytsev has been Chief Scientific Officer of Institute of Medico-Ecological Problems in Barnaul, Russia, which was responsible for studying the health and environmental effects of the former Soviet Union's nuclear testing program. Under his leadership the Institute developed into an internationally recognized research center. <br/><br/>  
                Dr. Zaytsev is an active business community leader, a founder and the first president of US-Russia Technology Symposium at Stanford University, and the annual Silicon Valley Open Doors investment conference. Dr. Zaytsev is an author of more than 70 scientific and business publications on entrepreneurship, venture capital and biotechnology. He was invited to present to the Presidential Commission for Modernization and Technological Development of Russia's Economy and various industry meetings. <br/><br/>   
                Evgeny Zaytsev received his M.D. (cum laude) in 1991 and his Ph.D. in physiology in 1993, both from the Altai State Medical University.  He earned his M.B.A. from the Stanford Graduate School of Business in 2002.                          
